function abrirConsejos() {
  document.getElementById("solapaDiv").style.visibility = "visible";
  document.getElementById("consejosDiv").style.visibility = "visible";
}

function cerrarConsejos() {
  document.getElementById("solapaDiv").style.visibility = "hidden";
  document.getElementById("consejosDiv").style.visibility = "hidden";
}

